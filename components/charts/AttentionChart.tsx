import React from "react";
import dynamic from "next/dynamic";
import { useTheme, CardContent, Card } from "@mui/material";
import { ApexOptions } from "apexcharts";
import { ChartContext } from "./FacialEmotionChart";

export interface EngagementData {
  [key: string]: string;
}
const ReactApexChart = dynamic(() => import("react-apexcharts"), { ssr: false });

interface AttentionChartProps {
  playAt: (timeStamp: number) => void;
}

const AttentionChart: React.FC<AttentionChartProps> = ({ playAt }) => {
  const theme = useTheme();
  const keys = Object.keys(engagement);
  const step = Math.ceil(keys.length / 6 / 25);
  const [dataPoints, fullCategories] = React.useMemo(() => {
    const dp: { x: string; y: number }[] = [];
    const fullCats: string[] = [];
    const keys = Object.keys(engagement);

    keys.forEach((key, index) => {
      if (index % 6 === 0) {
        const avg =
          ((parseFloat(engagement[key]) +
            parseFloat(engagement[String(parseInt(key) + 1)] || "0") +
            parseFloat(engagement[String(parseInt(key) + 2)] || "0")) /
            3) *
          100;

        const timeInSeconds = index / 6;
        const minutes = Math.floor(timeInSeconds / 60);
        const seconds = Math.floor(timeInSeconds % 60)
          .toString()
          .padStart(2, "0");
        const formattedTime = `${minutes}:${seconds}`;

        dp.push({ x: formattedTime, y: Math.round(avg) });
        fullCats.push(formattedTime);
      }
    });

    return [dp, fullCats];
  }, []);

  const series = [{ name: "Attention", data: dataPoints }];

  interface ChartEvent {
    event: React.MouseEvent;
    chartContext: ChartContext;
    config: {
      seriesIndex: number;
      dataPointIndex: number;
      w: {
        config: {
          series: {
            name: string;
            data: number[];
          }[];
        };
      };
    };
  }

  const options: ApexOptions = {
    chart: {
      height: 350,
      type: "line",
      zoom: { enabled: false },
      toolbar: { show: false },
      animations: {
        enabled: true,
        easing: "easeinout",
        speed: 800,
        animateGradually: { enabled: true, delay: 150 },
        dynamicAnimation: { enabled: true, speed: 350 },
      },
      events: {
        click: (
          event: React.MouseEvent,
          chartContext: ChartContext,
          config: ChartEvent["config"]
        ) => {
          // this will print mango, apple etc. when clicked on respective datapoint
          console.log("Event chartContext:", chartContext);
          console.log("Event event:", event);
          console.log("Event cmth:", config);

          const time_stamp = config.dataPointIndex;
          playAt(time_stamp);
        },
      },
    },
    stroke: {
      curve: "smooth",
    },
    xaxis: {
      title: {
        text: "Time",
        style: { color: theme.palette.text.primary },
      },
      labels: {
        style: { colors: theme.palette.text.primary },
        formatter: (val: string) => {
          if (typeof val !== "string" || !val.includes(":")) {
            return "";
          }
          const [minutes, seconds] = val.split(":").map(Number);
          const totalSeconds = minutes * 60 + seconds;

          return totalSeconds % step === 0 ? val : "";
        },
      },
    },

    yaxis: {
      title: {
        text: "Attention Level (%)",
        style: { color: theme.palette.text.primary },
      },
      labels: {
        style: { colors: theme.palette.text.primary },
        formatter: (val: number) => `${val.toFixed(0)}`,
      },
    },
    annotations: {
      yaxis: [
        {
          y: 90,
          y2: 100,
          borderColor: "rgba(76, 175, 80, 0.8)", // Slightly transparent green
          fillColor: "rgba(76, 175, 80, 0.8)",
          label: {
            text: "Optimal",
            style: {
              color: "#fff",
              background: "rgba(76, 175, 80, 1)",
            },
          },
        },
        {
          y: 70,
          y2: 89,
          borderColor: "rgba(255, 235, 59, 0.8)", // Slightly transparent yellow
          fillColor: "rgba(255, 235, 59, 0.8)",
          label: {
            text: "Recommended",
            style: {
              color: "#333",
              background: "rgba(255, 235, 59, 1)",
            },
          },
        },
        {
          y: 50,
          y2: 69,
          borderColor: "rgba(255, 120, 7, 0.8)", // Slightly transparent amber
          fillColor: "rgba(255, 120, 7, 0.9)",
          label: {
            text: "Warning",
            style: {
              color: "#fff",
              background: "rgba(255, 120, 7, 1)",
            },
          },
        },
        {
          y: 0,
          y2: 49,
          borderColor: "rgba(244, 67, 54, 0.8)", // Slightly transparent red
          fillColor: "rgba(244, 67, 54, 0.8)",
          label: {
            text: "Critical",
            style: {
              color: "#fff",
              background: "rgba(244, 67, 54, 1)",
            },
          },
        },
      ],
    },

    tooltip: {
      x: {
        formatter: function (val, { dataPointIndex }) {
          return fullCategories[dataPointIndex]; // Use fullCategories here for detailed tooltips
        },
      },
      y: {
        formatter: function (val: number) {
          return `${val.toFixed(0)}%`;
        },
      },
    },
  };

  return (
    <Card raised sx={{ mt: 2, mb: 2 }}>
      <CardContent>
        <ReactApexChart options={options} series={series} height={350} />
      </CardContent>
    </Card>
  );
};

export default AttentionChart;

