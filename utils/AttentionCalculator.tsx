import { EngagementData } from "@/components/charts/AttentionChart";

export function calculateAttentionScore(data: EngagementData): number {
  let totalEngagement = 0;
  let count = 0;

  for (const key in data) {
    // Use Object.prototype.hasOwnProperty.call to safely check for properties
    if (Object.prototype.hasOwnProperty.call(data, key)) {
      totalEngagement += parseFloat(data[key]); // Convert string to number and add to total
      count++; // Increment count for each entry
    }
  }

  return count > 0 ? Math.round((100 * totalEngagement) / count) : 0; // Calculate average
}

